// Configurar transi√ß√£o para login
function setupLoginTransition() {
    const loginBtn = document.getElementById('loginBtn');
    const registerBtn = document.getElementById('registerBtn');
    const backBtn = document.getElementById('backButton');
    const enterBtn = document.getElementById('enterBtn');
    const newRaidBtn = document.getElementById('newRaidBtn');
    const raidsBtn = document.getElementById('raidsBtn');
    
    // Configurar o bot√£o entrar inicialmente para login
    enterBtn.onclick = handleLogin;
    
    if (loginBtn) {
        loginBtn.addEventListener('click', function(e) {
            e.preventDefault();
            enterBtn.textContent = 'Entrar';
            enterBtn.onclick = handleLogin;
            transitionToLogin();
        });
    }
    
    if (registerBtn) {
        registerBtn.addEventListener('click', function(e) {
            e.preventDefault();
            enterBtn.textContent = 'Registrar-se';
            enterBtn.onclick = handleRegistration;
            transitionToLogin();
        });
    }
    
    // Na fun√ß√£o setupLoginTransition, atualize esta parte:
if (backBtn) {
    backBtn.addEventListener('click', function(e) {
        e.preventDefault();
        const menuContainer = document.getElementById('menuContainer');
        const uploadContainer = document.getElementById('uploadContainer');
        
        // Se estiver no upload, N√ÉO FAZ NADA (bot√£o est√° escondido)
        if (uploadContainer.style.display === 'block' || uploadContainer.classList.contains('fade-in-upload')) {
            return; // N√£o faz nada, o bot√£o est√° escondido
        } 
        // Se estiver no menu, volta para login
        else if (menuContainer.style.display === 'block' || menuContainer.classList.contains('fade-in')) {
            transitionBackToLogin();
        } else {
            // Se estiver no login, volta para home
            transitionToHome();
        }
    });
}
    
    // Configurar bot√µes do menu
    if (newRaidBtn) {
        newRaidBtn.addEventListener('click', function(e) {
            e.preventDefault();
            transitionToUpload(); // Nova fun√ß√£o para upload
        });
    }
    
    if (raidsBtn) {
        raidsBtn.addEventListener('click', function(e) {
            e.preventDefault();
            showPopup('üìã Carregando raids...');
        });
    }
}

// Nova fun√ß√£o para configurar o upload
function setupUploadFunctionality() {
    const uploadArea = document.getElementById('uploadArea');
    const fileInput = document.getElementById('fileInput');
    const confirmUploadBtn = document.getElementById('confirmUploadBtn');
    const cancelUploadBtn = document.getElementById('cancelUploadBtn');
    
    if (uploadArea && fileInput) {
        // Click na √°rea de upload
        uploadArea.addEventListener('click', function() {
            fileInput.click();
        });
        
        // Altera√ß√£o no input de arquivo
        fileInput.addEventListener('change', handleFileUpload);
        
        // Drag and drop
        uploadArea.addEventListener('dragover', function(e) {
            e.preventDefault();
            uploadArea.classList.add('dragover');
        });
        
        uploadArea.addEventListener('dragleave', function() {
            uploadArea.classList.remove('dragover');
        });
        
        uploadArea.addEventListener('drop', function(e) {
            e.preventDefault();
            uploadArea.classList.remove('dragover');
            
            if (e.dataTransfer.files.length) {
                fileInput.files = e.dataTransfer.files;
                handleFileUpload();
            }
        });
    }
    
    if (confirmUploadBtn) {
        confirmUploadBtn.addEventListener('click', confirmUpload);
    }
    
    if (cancelUploadBtn) {
        cancelUploadBtn.addEventListener('click', function() {
            transitionBackToMenuFromUpload();
        });
    }
}

